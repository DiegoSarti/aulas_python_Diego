# substituir para entrada do Usuario (input)
numeroUm = 10
numeroDois = 5
resultado = 0 #variavel para receber o resultado da operação
# soma
resultado = numeroUm + numeroDois
print(numeroUm,'+', numeroDois,'=', resultado)
# subtração
resultado = numeroUm - numeroDois
print(numeroUm,'-', numeroDois,'=',resultado)
# multiplicação
resultado = numeroUm * numeroDois
print(numeroUm,'*', numeroDois,'=',resultado)
#divisão
resultado = numeroUm / numeroDois
print(numeroUm,'/', numeroDois,'=',resultado)
